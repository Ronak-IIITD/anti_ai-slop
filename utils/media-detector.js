// AI Media Detector v1
// Lightweight, privacy-first heuristics for AI-likely images and videos
// Updated as of 2026-03-01

const AI_MEDIA_KEYWORDS = {
  strong: [
    'ai generated',
    'ai-made',
    'midjourney',
    'stable diffusion',
    'sdxl',
    'dall-e',
    'dalle',
    'flux',
    'firefly',
    'runway',
    'veo',
    'sora',
    'pika',
    'leonardo ai',
    'generated with ai',
    'made with ai',
    'synthetic media'
  ],
  moderate: [
    'prompt',
    'prompted',
    'text to image',
    'text-to-image',
    'text to video',
    'text-to-video',
    'cinematic ai',
    'hyperreal',
    'ultra realistic',
    '8k render',
    'rendered',
    'digital render',
    'concept render'
  ]
};

const AI_MEDIA_WATERMARK_HINTS = [
  'midjourney',
  'runway',
  'veo',
  'sora',
  'pika',
  'generated by ai',
  'made with ai'
];

class AIMediaDetector {
  analyzeElement(element, context = {}) {
    const reasons = [];
    let score = 0;

    const textSignals = this.collectTextSignals(element, context);
    const lowered = textSignals.toLowerCase();

    const strongMatches = AI_MEDIA_KEYWORDS.strong.filter(keyword => lowered.includes(keyword));
    const moderateMatches = AI_MEDIA_KEYWORDS.moderate.filter(keyword => lowered.includes(keyword));

    if (strongMatches.length > 0) {
      score += 35 + Math.min((strongMatches.length - 1) * 10, 20);
      reasons.push('ai-media-keywords');
    }

    if (moderateMatches.length >= 2) {
      score += 18;
      reasons.push('multiple-media-hints');
    } else if (moderateMatches.length === 1) {
      score += 8;
      reasons.push('some-media-hints');
    }

    const media = this.collectMediaInfo(element);
    if (media.hasMultipleImages) {
      score += 6;
      reasons.push('image-heavy-card');
    }
    if (media.hasVideo) {
      score += 6;
      reasons.push('video-present');
    }
    if (media.hasShortSyntheticAlt) {
      score += 12;
      reasons.push('synthetic-alt-text');
    }
    if (media.hasWatermarkHint) {
      score += 18;
      reasons.push('watermark-hint');
    }
    if (media.hasGlossyRenderTerms) {
      score += 10;
      reasons.push('render-language');
    }

    if (context.channelName) {
      const channelLower = context.channelName.toLowerCase();
      if (AI_MEDIA_WATERMARK_HINTS.some(keyword => channelLower.includes(keyword))) {
        score += 12;
        reasons.push('channel-ai-branding');
      }
    }

    return {
      score: Math.min(score, 100),
      reasons,
      matchedKeywords: [...strongMatches, ...moderateMatches]
    };
  }

  async analyzeElementAsync(element, context = {}, options = {}) {
    const base = this.analyzeElement(element, context);
    const enableOcr = options.enableOcr === true;

    if (!enableOcr || typeof TextDetector === 'undefined') {
      return base;
    }

    try {
      const detectedText = await this.extractTextFromImages(element);
      if (detectedText) {
        const lowered = detectedText.toLowerCase();
        const keywordMatches = AI_MEDIA_KEYWORDS.strong.filter(keyword => lowered.includes(keyword));
        if (keywordMatches.length > 0) {
          base.score = Math.min(base.score + 25, 100);
          base.reasons.push('ocr-watermark');
          base.matchedKeywords.push(...keywordMatches);
        }
      }
    } catch (error) {
      // OCR is best-effort only
    }

    return base;
  }

  collectTextSignals(element, context = {}) {
    const chunks = [];
    const push = value => {
      if (value && typeof value === 'string') {
        chunks.push(value.trim());
      }
    };

    push(context.title);
    push(context.description);
    push(context.channelName);

    const text = element?.textContent?.trim();
    if (text) {
      push(text.slice(0, 500));
    }

    if (element) {
      element.querySelectorAll('img, video, source').forEach(node => {
        push(node.getAttribute('alt'));
        push(node.getAttribute('aria-label'));
        push(node.getAttribute('title'));
        push(node.getAttribute('data-testid'));
      });
    }

    return chunks.join(' ').toLowerCase();
  }

  collectMediaInfo(element) {
    const info = {
      hasMultipleImages: false,
      hasVideo: false,
      hasShortSyntheticAlt: false,
      hasWatermarkHint: false,
      hasGlossyRenderTerms: false
    };

    if (!element) {
      return info;
    }

    const images = Array.from(element.querySelectorAll('img'));
    const videos = Array.from(element.querySelectorAll('video'));
    info.hasMultipleImages = images.length >= 2;
    info.hasVideo = videos.length > 0;

    const attributes = [];
    images.forEach(img => {
      attributes.push(img.alt || '');
      attributes.push(img.getAttribute('aria-label') || '');
      attributes.push(img.getAttribute('title') || '');
      attributes.push(img.src || '');
    });
    videos.forEach(video => {
      attributes.push(video.getAttribute('poster') || '');
      attributes.push(video.getAttribute('aria-label') || '');
      attributes.push(video.getAttribute('title') || '');
    });

    const blob = attributes.join(' ').toLowerCase();
    if (/ai (image|art|photo|video)|generated image|synthetic/i.test(blob)) {
      info.hasShortSyntheticAlt = true;
    }

    if (AI_MEDIA_WATERMARK_HINTS.some(keyword => blob.includes(keyword))) {
      info.hasWatermarkHint = true;
    }

    if (/8k render|octane|hyperreal|ultra realistic|cinematic render|concept art/i.test(blob)) {
      info.hasGlossyRenderTerms = true;
    }

    return info;
  }

  async extractTextFromImages(element) {
    if (!element) return '';

    const images = Array.from(element.querySelectorAll('img'))
      .filter(img => img.naturalWidth >= 120 && img.naturalHeight >= 120)
      .slice(0, 2);

    if (images.length === 0) return '';

    const detector = new TextDetector();
    const chunks = [];

    for (const img of images) {
      try {
        if (img.decode) {
          await img.decode();
        }
        const results = await detector.detect(img);
        results.forEach(result => {
          if (result.rawValue) chunks.push(result.rawValue);
        });
      } catch (error) {
        // Ignore OCR errors (often CORS or unsupported)
      }
    }

    return chunks.join(' ').trim();
  }

  shouldWarn(score, sensitivity = 'medium') {
    const threshold = this.getThreshold(sensitivity);
    return score >= threshold;
  }

  getThreshold(sensitivity) {
    switch (sensitivity) {
      case 'low':
        return 72;
      case 'high':
        return 42;
      case 'medium':
      default:
        return 56;
    }
  }
}

const aiMediaDetector = new AIMediaDetector();

if (typeof window !== 'undefined') {
  window.aiMediaDetector = aiMediaDetector;
}
